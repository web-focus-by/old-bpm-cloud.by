export const getSubTitleText = title => {
  switch (title) {
    case "Разработка сайтов":
      return "Разработка сайтов"
    case "SEO":
      return "SEO-продвижение"
    case "Брендинг и дизайн":
      return "Брендинг и дизайн"
    case "PPC":
      return "Контекстная реклама"
    case "SMM":
      return "SMM-продвижение"
    case "Разработка приложений":
      return "Разработка приложений"
    default:
      return "Реально работающий интернет-маркетинг"
  }
}
